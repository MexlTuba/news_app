class Author {
  final String authorImage;
  final String fname;
  final String lname;
  final String occupation;
  final String bio;
  final String followers;
  final String posts;
  final String following;

  Author(
      {required this.authorImage,
      required this.fname,
      required this.lname,
      required this.occupation,
      required this.bio,
      required this.followers,
      required this.posts,
      required this.following});
}
